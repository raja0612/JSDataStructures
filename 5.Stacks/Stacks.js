var stack = [];


// add item at end
stack.push("Instagram");
stack.push("TikTok");
stack.push("Facebook");
stack.push("Twitter");

//renove from end
stack.pop();

// add item at begining
stack.unshift("First");
stack.unshift("Delete All");

//remove item from begining
stack.shift();

// STACK works on LIFO principle.
